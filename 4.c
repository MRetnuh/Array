#include<stdio.h>
   #include<stdlib.h>
#include<time.h>
#define CANT 100
#define RANGO 100
int main(int argc, char *argv[]){
     srand(time(NULL));
       int numeros[CANT];
    double suma = 0.0;
     for(int i = 0; i < CANT; i++){
       numeros[i] = rand () % RANGO;
     suma = suma + numeros[i];
     }
     double promedio = suma / CANT;
    printf("el promedio es %.2f\n", promedio);
  return 0;
  }
